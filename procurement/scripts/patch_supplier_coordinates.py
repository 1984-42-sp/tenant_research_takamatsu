def main():
    df = pd.read_csv(IN_PATH, encoding="utf-8-sig").fillna("")

    patched = 0
    for idx, row in df.iterrows():
        addr = str(row["address_candidate"]).strip()
        if addr in PATCH:
            lat, lon = PATCH[addr]
            df.at[idx, "latitude"] = lat
            df.at[idx, "longitude"] = lon
            patched += 1

    df.to_csv(OUT_PATH, index=False, encoding="utf-8-sig")

    ok = df[(df["latitude"].astype(str) != "") & (df["longitude"].astype(str) != "")]
    ng = df[(df["latitude"].astype(str) == "") | (df["longitude"].astype(str) == "")]

    print("[PATCHED]", patched)
    print("[OK]", len(ok))
    print("[NG]", len(ng))
    print("\n[NG LIST]")
    print(ng[["supplier_id", "name", "address_candidate"]].to_string(index=False))

if __name__ == "__main__":
    main()